package JavaReactCampTask2;

public class Course {
	
	int id;
	String title;
	String subtitle;
	String image;
	String state;
	
	public Course() {
		
	}
	
	public Course(int id, String title, String subtitle, String image, String state) {
		super();
		this.id = id;
		this.title = title;
		this.subtitle = subtitle;
		this.image = image;
		this.state = state;
	}
	
	

}